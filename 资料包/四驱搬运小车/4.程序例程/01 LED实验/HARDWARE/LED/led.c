#include "led.h"


void LED_Init(void)
{
	RCC->APB2ENR|=1<<0;			//使能辅助时钟
	RCC->APB2ENR|=1<<5;    	//使能PORTD时钟	   	 
	GPIOD->CRL&=0XFFFFF0FF; 
	GPIOD->CRL|=0X00000300;	//PD2 推挽输出   	 
  GPIOD->ODR|=1<<2;     	 //PD2 输出高 	  
}

